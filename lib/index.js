let capitals = require('./capitals.js')


function buildJSONData(countries) {
	var rearrangedObject = {};
	
	countries.forEach(function (countryData) {

		let elem = capitals.capitals.find(function (element) {
			return element.country == countryData.name;
		});
		Object.assign(rearrangedObject, {
			[countryData.name.toLowerCase()]: {
				countryCode: countryData.code,
				capital: elem != null ? elem.city : ""
			}
		})
	});
	return rearrangedObject;
}


module.exports = buildJSONData(africanCountries);